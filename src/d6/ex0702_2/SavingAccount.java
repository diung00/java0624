package d6.ex0702_2;

public class SavingAccount extends BankAccount{
    private int round;
    private int eddRound;
    private int amountOfRound;



    public int getRound() {
        return round;
    }

    public void setRound(int round) {
        this.round = round;
    }

    public int getEddRound() {
        return eddRound;
    }

    public void setEddRound(int eddRound) {
        this.eddRound = eddRound;
    }

    public int getAmountOfRound() {
        return amountOfRound;
    }

    public void setAmountOfRound(int amountOfRound) {
        this.amountOfRound = amountOfRound;
    }
}
